package com.operators;

public class Relational_Operators {
	public static void main(String[] args) {
		int a = 10, b = 20;
		
		System.out.println("true/false: " +(a>b)); //f
		System.out.println("true/false: " +(a<b)); //t
		System.out.println("true/false: " +(a>=b)); //f
		System.out.println("true/false: " +(a<=b)); //t
		System.out.println("true/false: " +(a!=b)); //t
		System.out.println("true/false: " +(a==b)); //f
		
	}
}
